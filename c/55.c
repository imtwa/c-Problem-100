/*
学习使用按位取反~
~0=1; ~1=0;
数字在计算机里面都是用二进制补码储存 
*/

#include <stdio.h>
int main() {
	int a,b;
	a=0b0011;
	b=~a;	// b=1100(此时为补码形式)转为源码
			//（补码最高位为符号位 剩余位取反后+1） 1,100(-4) 
	printf("a 的按位取反值为（十进制） %d \n",b);
	a=~a;// -4 源码为-0000 0100 转为补码（源码取反后+1）1111 1100 (fc) 
	printf("a 的按位取反值为（十六进制） %x \n",a);
	return 0;
}
